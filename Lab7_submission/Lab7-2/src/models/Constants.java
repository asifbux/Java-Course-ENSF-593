package models;
/**
 * Provide final constants to for tic-tac-toe 
 * board game in a Java application.
 * The overall purpose of this class exercise is to give players access to the X or O mark 
 * which is possible when the Class Game and Board have the access to these final constants.
 * @author Asif Bux
 * @version 1.0
 * @since November 5, 2019
 */
public interface Constants {
	
	/** The Constant SPACE_CHAR of type Character of one empty space. */
	static final char SPACE_CHAR = ' ';
	
	/** The Constant LETTER_O of type Character which holds mark X. */
	static final char LETTER_O = 'O';
	
	/** The Constant LETTER_X of type Character which holds mark O. */
	static final char LETTER_X = 'X';
}
